public class Advogado {
    private String oab;

    public Advogado(String oab) {
        this.oab = oab;
    }

    public String getOab() {
        return oab;
    }

    public void setOab(String oab) {
        this.oab = oab;
    }

    @Override
    public String toString() {
        return "\nOAB: " + oab +
               super.toString();
    }
    
    
}
